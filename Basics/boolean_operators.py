# And
print("And")
# True and True -> True
print(True and True)
# True and False -> False
print(True and False)
# False and False -> False
print(False and False)

# Or
print("\nOr")
# True or True -> True
print(True or True)
# True or False -> True
print(True or False)
# False or False -> False
print(False or False)

# Not
print("\nNot")
# not True -> False
print(not True)
# not False -> True
print(not False)

print("\nComparators with boolean operators:")
and_example = (10 > 5) and (5 > 3)
print(and_example)

or_example = (10 > 5) or (5 < 3)
print(or_example)

not_example = not (10 > 5)
print(not_example)